package ecnic.service.common.security;

/**
 * The enum User role.
 */
public enum UserRole {
    /**
     * Super admin user role.
     */
    SUPER_ADMIN,
    /**
     * Admin user role.
     */
    ADMIN,
    /**
     * Owner user role.
     */
    OWNER,
    /**
     * Employee user role.
     */
    EMPLOYEE,
    /**
     * Stranger user role.
     */
    STRANGER,
}
